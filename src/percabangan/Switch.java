package percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("urang joging");
                break;
            case 2:
                System.out.println("urang besapida");
                break;
            case 3:
                System.out.println("urang bejalan wara");
                break;
            case 4:
                System.out.println("urang urang bedua laki bini");
                break;
            default:
                System.out.println("kuntilanak");
                break;
        }
    }
}
